function mindGame(input) {
  let multiply = input * 3;
  let add = multiply + 10;
  let divide = add / 2;
  let subtract = divide - 5;
  return subtract;
}

//                                          second problem

function evenOdd(input) {
  let len = input.length;
  if (len % 2 !== 0) {
    return "odd";
  } else {
    return "even";
  }
}

//                                           third problem

function isLGSeven(input) {
  let subtract = input - 7;

  if (subtract < 7) {
    return subtract;
  } else {
    let output = subtract * 2;
    return output;
  }
}

